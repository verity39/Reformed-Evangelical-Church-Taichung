如是，父全能，子全能，聖靈亦全能。
然而，非三全能者，乃一全能者。
如是，父是神，子是神，聖靈亦是神。
然而，非三神，乃一神。
如是，父是主，子是主，聖靈亦是主。
然而，非三主，乃一主。
依基督真道，我等不得不認三位均為神，均為主。
依大公教，我等亦不得謂神有三，亦不得謂主有三。
父非由誰作成：既非受造，亦非受生。
子獨由于父：非作成，亦非受造；而為受生。
聖靈由于父與子：既非作成，亦非受造，亦非受生；而為發出。
