凡人欲得救，首先當持守大公教會信仰。
此信仰，凡守之不全不正者，必永遠沉淪。
大公教會信仰即：我等敬拜一體三位，而三位一體之神。
其位不紊，其體不分。
父一位，子一位，聖靈亦一位。
然而父子聖靈同一神性，同一榮耀，亦同一永恆之尊嚴。
父如何，子如何，聖靈亦如何。
父不受造，子不受造，聖靈亦不受造。
父無限，子無限，聖靈亦無限。
父永恆，子永恆，聖靈亦永恆。
