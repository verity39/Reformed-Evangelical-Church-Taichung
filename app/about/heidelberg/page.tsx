（二）如果他還是不聽，他們就要禁止他領聖餐12，把他摒除在教會之外；同時，神也把他摒除於教會、基督的國度之外；
（三）但是如果他答應悔改，並且有真實的表現，就可以重新接納他，作為基督和祂教會的肢體13。

