主日50
問125：第四項祈求是什麼？
答：是「我們日用的飲食，今日賜給我們」1；也就是：
（一）求主欣然賜給我們身體一切的需要2，好叫我們知道祂是一切美善賞賜的源頭3；
（二）如果祂不賜福，我們一切籌算、勞苦、恩賜，都不能使我們得益處4，所以我們不依靠受造物，惟獨靠祂5。

