主日51
問126：第五項祈求是什麼？
答：是「免我們的債，如同我們免了人的債」1；也就是：
（一）懇求主因基督寶血的緣故，不將我們的眾多過犯，歸於我們這些可悲的罪人身上，也不將那仍舊殘留在我們身上的邪惡歸於我們2；
（二）我們怎樣經歷祂這樣恩待我們，我們也願意真心赦免我們的鄰舍3。

