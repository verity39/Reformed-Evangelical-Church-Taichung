主日49
問124：第三項祈求是什麼？
答：是「願祢的旨意行在地上，如同行在天上」1；也就是：
（一）求主叫我們和眾人捨棄自己的意思2，並且把自己的意思降服在祂惟一美好的旨意之下，不討價還價3；
（二）好使每個人都可以完成自己的職分和呼召4，甘心而真誠，好像天上的天使一樣5。

